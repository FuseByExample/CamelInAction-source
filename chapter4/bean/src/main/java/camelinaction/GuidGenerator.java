package camelinaction;

import java.util.Random;

/**
 * Generates an unique random id
 *
 * @version $Revision: 56 $
 */
public class GuidGenerator {

    public static int generate() {
        Random ran = new Random();
        return ran.nextInt(10000000);
    }

}
